// Operators in JavaScript 

// 1. Arithmetic
let a = 10 + 5;   // + - * / % **

// 2. Assignment
let b = 10;       // = += -= *= /= %=

// 3. Comparison
let c = 10 == "10";   // == === != !== > < >= <=

// 4. Logical
let d = true && false; // && || !

// 5. Unary
let e = 5;
e++; // ++ -- typeof

// 6. Ternary
let f = (10 > 5) ? "Yes" : "No";

// 7. Nullish Coalescing
let g = null ?? "default";

// 8. Optional Chaining
let h = obj?.name;

// 9. Bitwise
let i = 5 & 1; // & | ^ ~ << >> >>>

// 10. String
let j = "Hello" + " World";

// 11. Type
let k = typeof 10; // typeof, instanceof
